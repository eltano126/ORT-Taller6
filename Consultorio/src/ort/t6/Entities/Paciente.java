package ort.t6.Entities;

public class Paciente {
	int pa_dni;
	String pa_nombre;
	String pa_apellido;
	String pa_sexo;
	String pa_nacimiento;
	String pa_direccion;
	int pa_telefono;
	int pa_celular;
	String pa_mail;
	
	public int getPa_dni() {
		return pa_dni;
	}
	public void setPa_dni(int pa_dni) {
		this.pa_dni = pa_dni;
	}
	public String getPa_nombre() {
		return pa_nombre;
	}
	public void setPa_nombre(String pa_nombre) {
		this.pa_nombre = pa_nombre;
	}
	public String getPa_apellido() {
		return pa_apellido;
	}
	public void setPa_apellido(String pa_apellido) {
		this.pa_apellido = pa_apellido;
	}
	public String getPa_sexo() {
		return pa_sexo;
	}
	public void setPa_sexo(String pa_sexo) {
		this.pa_sexo = pa_sexo;
	}
	public String getPa_nacimiento() {
		return pa_nacimiento;
	}
	public void setPa_nacimiento(String pa_nacimiento) {
		this.pa_nacimiento = pa_nacimiento;
	}
	public String getPa_direccion() {
		return pa_direccion;
	}
	public void setPa_direccion(String pa_direccion) {
		this.pa_direccion = pa_direccion;
	}
	public int getPa_telefono() {
		return pa_telefono;
	}
	public void setPa_telefono(int pa_telefono) {
		this.pa_telefono = pa_telefono;
	}
	public int getPa_celular() {
		return pa_celular;
	}
	public void setPa_celular(int pa_celular) {
		this.pa_celular = pa_celular;
	}
	public String getPa_mail() {
		return pa_mail;
	}
	public void setPa_mail(String pa_mail) {
		this.pa_mail = pa_mail;
	}
}
