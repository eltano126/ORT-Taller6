package ort.t6.Entities;

public class Medico {
	
	int me_matricula; 	
	String me_usuario; 		
	String me_pass; 		
	String me_nombre; 		
	String me_apellido; 	
	
	public int getMe_matricula() {
		return me_matricula;
	}
	public void setMe_matricula(int me_matricula) {
		this.me_matricula = me_matricula;
	}
	public String getMe_usuario() {
		return me_usuario;
	}
	public void setMe_usuario(String me_usuario) {
		this.me_usuario = me_usuario;
	}
	public String getMe_pass() {
		return me_pass;
	}
	public void setMe_pass(String me_pass) {
		this.me_pass = me_pass;
	}
	public String getMe_nombre() {
		return me_nombre;
	}
	public void setMe_nombre(String me_nombre) {
		this.me_nombre = me_nombre;
	}
	public String getMe_apellido() {
		return me_apellido;
	}
	public void setMe_apellido(String me_apellido) {
		this.me_apellido = me_apellido;
	}
}
