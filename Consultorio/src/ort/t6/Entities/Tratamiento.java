package ort.t6.Entities;

public class Tratamiento {
	
	int tr_id;
	String tr_nombre;
	String tr_descripcion;
	int tr_duracion;
	
	public int getTr_id() {
		return tr_id;
	}
	public void setTr_id(int tr_id) {
		this.tr_id = tr_id;
	}
	public String getTr_nombre() {
		return tr_nombre;
	}
	public void setTr_nombre(String tr_nombre) {
		this.tr_nombre = tr_nombre;
	}
	public String getTr_descripcion() {
		return tr_descripcion;
	}
	public void setTr_descripcion(String tr_descripcion) {
		this.tr_descripcion = tr_descripcion;
	}
	public int getTr_duracion() {
		return tr_duracion;
	}
	public void setTr_duracion(int tr_duracion) {
		this.tr_duracion = tr_duracion;
	}

}
